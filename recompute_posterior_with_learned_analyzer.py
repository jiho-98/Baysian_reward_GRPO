#!/usr/bin/env python3
"""Recompute Bayesian posterior rewards using a learned unified analyzer.

This script reads an existing bayesian_reward_debug.jsonl file generated by
Bayesian Full GRPO, then uses a learned unified analyzer LoRA adapter to
re-judge:

1. prior_judge:
   problem + candidate strategies -> suitability JSON

2. evidence_judge:
   problem + strategy + reasoning + final answer + correctness -> evidence JSON

The prompt bodies are matched to prepare_analyzer_training_data.py.
By default, the same task prefix style used for unified multi-task SFT is
prepended. Use --no_task_prefix to disable that prefix.

Outputs:
- learned_posterior_debug.jsonl
- group_metrics.jsonl
- summary.json
"""

